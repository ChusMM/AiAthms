class coordenada:
    def __init__(self, vertice = '', latitud = 0.0, longitud = 0.0):
        self.vertice = vertice
        self.latitud = latitud
        self.longitud = longitud
    def Vertice(self):
        return self.vertice
    def Latitud(self):
        return self.latitud
    def Longitud(self):
        return self.longitud